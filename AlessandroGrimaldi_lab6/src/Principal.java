        float sueldo;
        JTree model = tree1;

        sueldo=Float.parseFloat(tf_sueldo.getText());
        nombre = tf_nombre.getText();
        edad = Integer.parseInt(tf_edad.getText());
        id=tf_id.getText();
        nacionalidad=tf_nacionalidad.getText();
